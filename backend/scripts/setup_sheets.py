def main() -> None:
    creds = Credentials.from_service_account_file(settings.google_credentials_path, scopes=SCOPES)
    client = gspread.authorize(creds)
    sheet = client.open_by_key(settings.google_spreadsheet_id)

    existing = {ws.title for ws in sheet.worksheets()}

    for name, headers in WORKSHEETS.items():
        if name in existing:
            print(f"[=] {name}: вкладка уже есть, пропускаю")
            continue
        ws = sheet.add_worksheet(title=name, rows=1000, cols=len(headers))
        ws.append_row(headers)
        print(f"[+] {name}: создана, заголовки {headers}")

    if "tax_values" in existing:
        print("[=] tax_values: вкладка уже есть, пропускаю")
    else:
        ws = sheet.add_worksheet(title="tax_values", rows=10, cols=len(TAX_VALUES_HEADERS))
        ws.append_rows([TAX_VALUES_HEADERS, *TAX_VALUES_SEED])
        print(f"[+] tax_values: создана и заполнена ({len(TAX_VALUES_SEED)} режимов)")

    if "step_tax_value" in existing:
        print("[=] step_tax_value: вкладка уже есть, пропускаю")
    else:
        ws = sheet.add_worksheet(title="step_tax_value", rows=10, cols=len(STEP_TAX_VALUE_HEADERS))
        ws.append_rows([STEP_TAX_VALUE_HEADERS, *STEP_TAX_VALUE_SEED])
        print(f"[+] step_tax_value: создана и заполнена ({len(STEP_TAX_VALUE_SEED)} алгоритмов)")


if __name__ == "__main__":
    main()
